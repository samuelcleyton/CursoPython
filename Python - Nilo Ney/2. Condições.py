'''
Estruturas condicionais, conceitos de blocos e seleção de linhas a executar com base na avaliação de expressões lógicas.

Condições
    Nem sempre todas as linha dos programas serão executadas. 
    Muitas vezes, será mais interessante decidir que partes do programa devem ser executadas
    com base no resultado de uma condição. 

IF
    As condições servem para selecionar quando uma parte do programa deve ser ativada 
    e quando deve ser simplesmente ignorada.

Formato de estrutura de condicional if
    if <condição>:
        bloco verdadeiro
    O if nada mais é que o nosso "se". 
    Se a condição for verdadeira, faça alguma coisa. 

a = int(input('Primeiro valor: '))
b = int(input('Segundo valor: '))
if a > b:
    print('O primeiro número é maior!')
if b > a:
    print('O segundo número é maior!')

ELSE
    Quando há problemas, onde a segunda condição é simplismente o inverso da primeira, 
    podemos usar else.

Estruturas aninhadas
    Nem sempre nossos programas serão tão simples.
    Muitas vezes, precisaremos aninhar vários if para obter o comportamento desejado do programa.
    Aninhar é utilizar um if dentro de outro. 

ELIF
    Solução para multiplos ifs aninhados.
    Evita problemas de deslocamentos desnecessários à direita. 
    






























































'''