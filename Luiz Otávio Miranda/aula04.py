# Tipos de Dados
# int e float
# O tipo int representa qualquer número (positivo ou negativo)
# float - número com ponto flutuante ou casa decimal (utilizamos o '.')

# A função type mostra o tipo do valor que será impresso


print('Samuel Cleyton Macena de Oliveira', type('Samuel Cleyton Macena de Oliveira'))
print(24, type(24))
print(1.69, type(1.69))
print(24 > 20, type(24 > 20))
