# A função print serve para imprimir valores na tela
# Argumentos nomeados e não-noemados

print(12, 34, sep='*')
print(45, 67, sep='-')

# \r\n -> CRLF serve para quebrar a linha 
# \n -> LF

print(12, 34, sep='-', end='\n')
print(45, 67)

# Print com letra maiúscula não funciona no python








print('Samuel', 'Cleyton', sep='*', end='*')
print('Macena', 'Oliveira', sep='*')

# o 'sep' serve para separar dentro da função print.
# o 'end' serve para quebrar a linha dentro da função.

print('017', '416', '154', sep='.', end='-')
print('96')



